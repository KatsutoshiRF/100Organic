public class GastoExtra {
    private String nomeGastoExtra;
    private int quantidadeGastoExtra;
    private double valorGastoExtra;

    public GastoExtra() {
    }

    public String getNomeGastoExtra() {
        return nomeGastoExtra;
    }

    public void setNomeGastoExtra(String nomeGastoExtra) {
        this.nomeGastoExtra = nomeGastoExtra;
    }

    public int getQuanGastoExtra() {
        return quantidadeGastoExtra;
    }

    public void setQuanGastoExtra(int quantidadeGastoExtra) {
        this.quantidadeGastoExtra = quantidadeGastoExtra;
    }

    public double getValorGastoExtra() {
        return valorGastoExtra;
    }

    public void setValorGastoExtra(double valorGastoExtra) {
        this.valorGastoExtra = valorGastoExtra;
    }
    
    
}
